let previouslyGenerated: string | null = null;

/**
 * Generate a unique (no two in a row) friendly worktree name.
 * Returns a hyphen-joined adjective-noun pair, e.g. "swift-otter".
 */
export function generateWorktreeName(): string {
  let name: string;
  do {
    const adj = ADJECTIVES[Math.floor(Math.random() * ADJECTIVES.length)]!;
    const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)]!;
    name = `${adj}-${noun}`;
  } while (name === previouslyGenerated);
  previouslyGenerated = name;
  return name;
}
